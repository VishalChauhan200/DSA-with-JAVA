class student{
    int rollno;

    String name;
}

public class H_OOPs{
    public static void main(String[] args) {
        student name1 = new student();
        name1.rollno = 14;
        name1.name = "vishal";
        System.out.println(name1.rollno);
        System.out.println(name1.name);

        student name2 = new student();
        name2.rollno = 15;
        name2.name = "Rishabh";
        System.out.println(name2.rollno);
        System.out.println(name2.name);
    }     
    
}
